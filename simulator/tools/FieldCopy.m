% Get fieldnames to be set
fname = fieldnames(f);

% Loop through all fields in f
for k = 1:numel(fname)

    % If field name already exists in s, go ahead and copy the
    % new value from f in.
    if isfield(s, fname{k})
        s.(fname{k}) = f.(fname{k});
    else
        error('Field ''%s'' does not exist in s', fname{k});
    end

end


