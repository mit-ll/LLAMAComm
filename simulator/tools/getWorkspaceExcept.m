if nargin == 1 && iscell(varargin{1})
  exceptions = varargin{1};
else
  exceptions = varargin;
end

wh = evalin('caller', 'who');

out = [];
if ~isempty(wh)
  for ii = 1:numel(wh)
    if ~any(strcmp(wh{ii}, exceptions))
      out.(wh{ii}) = evalin('caller', wh{ii});
    end
  end
end

