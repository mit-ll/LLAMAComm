if isnan(dd)
  error('Non-scalar inputs must be same size');
end

off = zeros(size(dd));
off(dd<4)  = 7.472 + 1.193*dd(dd<4);
off(dd>=4) = 12.976 - 0.13*dd(dd>=4);

slope = zeros(size(dd));
slope(dd<9)  = 1.44 + 0.57*dd(dd<9);
slope(dd>=9) = 6.719 - 0.053*dd(dd>=9);

SFdb = off + slope.*bels(ff/100);

SFdb(SFdb<0) = 0;


