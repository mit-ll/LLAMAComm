function flags = CheckRequestFlags(nodeobj)

% Function @node/CheckRequestFlags.m:
% Queries all modules within a node to see if the requests have
% been satisfied.
%
% USAGE: flags = CheckRequestFlags(nodeobj)
%
% Input argument:
%  nodeobj  (node obj) Node object with M modules
%
% Output argument:
%  flags    (1xM) Request flags for modules.
%            1=request pending, 0=request satisfied
%

